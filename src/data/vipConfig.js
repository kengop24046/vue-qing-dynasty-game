// 获取VIP等级信息
export const getVipInfo = (exp) => {
  for (let i = vipConfig.length - 1; i >= 0; i--) {
    if (exp >= vipConfig[i].expRequired) {
      return vipConfig[i]
    }
  }
  return vipConfig[0]
}
// 获取下一级所需经验
export const getNextLevelExp = (exp) => {
  const current = getVipInfo(exp)
  if (current.level >= 20) return 0
  return vipConfig[current.level + 1].expRequired
}